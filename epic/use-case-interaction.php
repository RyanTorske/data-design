<h1><strong>Interaction Flow</strong></h1>
<ul>
	<li>Tyler enters youtube.com into his browser.</li>
	<li>Server returns Youtube.com main page.</li>
	<li>Tyler finds a video from a producer he does not want to view.</li>
	<li>Tyler highlights the video with cursor and clicks on the ellipses on the bottom right corner of the video.</li>
	<li>Tyler moves his cursor to "Not Interested" or "Do not Recommend Channel.</li>
	<li>Tyler selects "Do Not Recommend Channel".</li>
	<li>Server reloads and hides all videos based from that channel, unless otherwise typed in directly.</li>
</ul>

